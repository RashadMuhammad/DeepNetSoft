import mongoose, { type Schema } from "mongoose"
import type { IMenu } from "../types"

const menuSchema: Schema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    description: {
      type: String,
      required: true,
      trim: true,
    },
    slug: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },
  },
  { timestamps: true },
)

export default mongoose.model<IMenu>("Menu", menuSchema)